
public class prefixPostfix {
	public static void main(String[] args) {
		/*
		 * String name = "Maria"; System.out.println(name + " Sharipova");
		 * System.out.println(name); name = name + " Burritt";
		 * System.out.println(name); int age = 10; System.out.println(age + 9);
		 * System.out.println(age); age = age + 40; System.out.println(age);
		 * System.out.println(age++); System.out.println(age);
		 * System.out.println(++age); System.out.println(age);
		 */
		int age = 10;
		System.out.println(age);
		System.out.println(++age);
		System.out.println(age);
		age = ++age;
		System.out.println(age);
	}
}
